// Get references to size and color buttons
const sizeButtons04 = document.querySelectorAll("#prestige .size-btn");
const colorButtons04 = document.querySelectorAll("#prestige .variant-btn");

// Function to update images based on selected size and color
function updateImages04(size, color) {
  const sizeObject = window[`pre__size_${size}`];

  const colorObject = sizeObject[color];

  if (sizeObject && colorObject) {
    // Set main image URL
    var mainImageElement = document.querySelector(
      "#prestige .images-block img"
    );
    if (mainImageElement) {
      mainImageElement.src = colorObject.mainImg;
      mainImageElement.srcset = colorObject.mainImg;
    }

    // Loop through sliders and set new src attributes
    var sliderImages = document.querySelectorAll("#prestige .slide-item img ");

    if (colorObject.sliders.length > 0) {
      for (var i = 0; i < sliderImages.length; i++) {
        var index = i % colorObject.sliders.length;
        var sliderImage = colorObject.sliders[index];

        if (sliderImages[i]) {
          sliderImages[i].src = sliderImage;
          sliderImages[i].srcset = sliderImage;
        }
      }
    }
  }
}

// Function to handle size button click
function handleSizeButtonClick04(size) {
  // Remove 'w--current' class from all size buttons
  sizeButtons04.forEach((button) => button.classList.remove("size__current"));

  // Add 'w--current' class to the clicked size button
  this.classList.add("size__current");

  const Color = document.querySelector("#prestige .variant-btn.active")
    .classList[2];

  // Update images based on the selected size and default color
  updateImages04(size, Color);
}

// Function to handle color button click
function handleColorButtonClick04(color) {
  // Remove 'active' class from all color buttons
  colorButtons04.forEach((button) => button.classList.remove("active"));

  // Add 'active' class to the clicked color button
  this.classList.add("active");

  // Get the selected size button with 'w--current' class
  const selectedSizeButton = document.querySelector(
    "#prestige .size-btn.size__current"
  );

  // If a size button is selected, update images based on the selected size and color
  if (selectedSizeButton) {
    const selectedSize = selectedSizeButton.innerText.trim().split("”")[0]; //output 72"
    updateImages04(selectedSize, color);
  }
}

// Attach click event listeners to size buttons
sizeButtons04.forEach((button) =>
  button.addEventListener("click", function () {
    handleSizeButtonClick04.call(this, this.innerText.trim().split("”")[0]);
  })
);

// Attach click event listeners to color buttons
colorButtons04.forEach((button) =>
  button.addEventListener("click", function () {
    handleColorButtonClick04.call(this, this.classList[2]); // Assuming the color class is the second class in the classList
  })
);
